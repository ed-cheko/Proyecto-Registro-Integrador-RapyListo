package pe.edu.upeu.tablas;


public class Territorios {
    String territorioId;
    String descripTerrito;
    String regionId;

    public String getTerritorioId() {
        return territorioId;
    }

    public void setTerritorioId(String territorioId) {
        this.territorioId = territorioId;
    }

    public String getDescripTerrito() {
        return descripTerrito;
    }

    public void setDescripTerrito(String descripTerrito) {
        this.descripTerrito = descripTerrito;
    }

    public String getRegionId() {
        return regionId;
    }

    public void setRegionId(String regionId) {
        this.regionId = regionId;
    }
     
}
